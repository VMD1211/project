package e;

public class Area {
	private String area;
	private int quantityCar, quantityMotor, quantityBike;
	private float price;

	public Area() {
		
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}

	public int getQuantityCar() {
		return quantityCar;
	}

	public void setQuantityCar(int quantityCar) {
		this.quantityCar = quantityCar;
	}

	public int getQuantityMotor() {
		return quantityMotor;
	}

	public void setQuantityMotor(int quantityMotor) {
		this.quantityMotor = quantityMotor;
	}

	public int getQuantityBike() {
		return quantityBike;
	}

	public void setQuantityBike(int quantityBike) {
		this.quantityBike = quantityBike;
	}

	public float getPrice() {
		return price;
	}

	public void setPrice(float price) {
		this.price = price;
	}

}
